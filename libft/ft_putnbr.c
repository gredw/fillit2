#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else if (n < 0 && n > -2147483648)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n >= 0 && n < 10)
		ft_putchar(n + 48);
	else if (n >= 0 && n <= 2147483647)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
}
