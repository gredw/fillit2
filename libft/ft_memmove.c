#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*src2;
	char	*dst2;
	size_t	i;

	src2 = (char *)src;
	dst2 = (char *)dst;
	i = len - 1;
	if (src < dst)
	{
		while ((int)i >= 0)
		{
			dst2[i] = src2[i];
			i--;
		}
	}
	else
	{
		i = 0;
		while (i < len)
		{
			dst2[i] = src2[i];
			i++;
		}
	}
	return (dst);
}
